interface GeocodeResult {
  lat: number;
  lng: number;
  displayName: string;
}

export async function geocodeAddress(city: string, state: string): Promise<GeocodeResult | null> {
  try {
    const query = `${city}, ${state}, United States`;
    const res = await fetch(
      `https://nominatim.openstreetmap.org/search?format=json&q=${encodeURIComponent(query)}&limit=1`,
      { headers: { 'User-Agent': 'JobBoardMVP/1.0' } }
    );
    const data = await res.json();
    if (data.length > 0) {
      return {
        lat: parseFloat(data[0].lat),
        lng: parseFloat(data[0].lon),
        displayName: data[0].display_name,
      };
    }
    return null;
  } catch {
    return null;
  }
}

export function normalizeState(input: string): string {
  const lower = input.trim().toLowerCase();
  if (Object.values(STATE_ABBREVS).map(v => v.toLowerCase()).includes(lower)) {
    return input.trim().toUpperCase();
  }
  return STATE_ABBREVS[lower] || input.trim();
}
